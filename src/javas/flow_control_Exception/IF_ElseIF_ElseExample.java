package javas.flow_control_Exception;


public class IF_ElseIF_ElseExample {
    public static void main(String[] args) {
        int i = 20;
        if (i > 18){
            System.out.println("i more than 18");
        }
        else if(i > 12){
            System.out.println("i more than 12 and less than 19");
        }
        else{
            System.out.println("i less than or equal 12");
        }

    }

}
